#include <stdio.h>

int main() {
    int a = 5;
    int b = 10;

    a += 5;  // Equivalent to a = a + 5
    b -= 3;  // Equivalent to b = b - 3

    printf("Value of a: %d\n", a);
    printf("Value of b: %d\n", b);

    return 0;
}
// This code demonstrates the use of compound assignment operators in C